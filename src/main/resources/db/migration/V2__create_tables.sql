CREATE TABLE RESIDUO (
  ID_RESIDUO        NUMBER GENERATED BY DEFAULT AS IDENTITY,
  TIPO_RESIDUO      VARCHAR2(30)  NOT NULL,
  DESCRICAO         VARCHAR2(200),
  CONSTRAINT PK_RESIDUO PRIMARY KEY (ID_RESIDUO),
  CONSTRAINT UQ_RESIDUO_TIPO UNIQUE (TIPO_RESIDUO)
);

CREATE TABLE PONTO_COLETA (
  ID_PONTO_COLETA   NUMBER GENERATED BY DEFAULT AS IDENTITY,
  NOME_PONTO        VARCHAR2(100) NOT NULL,
  CAPACIDADE_KG     NUMBER(12,2)  NOT NULL,
  VOLUME_ALERTA_KG  NUMBER(12,2)  NOT NULL,
  VOLUME_OCUPADO_KG NUMBER(12,2)  DEFAULT 0 NOT NULL,
  STATUS_PONTO      VARCHAR2(20)  NOT NULL,
  DT_ATUALIZACAO    DATE          DEFAULT SYSDATE NOT NULL,
  CONSTRAINT PK_PONTO_COLETA PRIMARY KEY (ID_PONTO_COLETA),
  CONSTRAINT CK_PONTO_STATUS CHECK (STATUS_PONTO IN ('DISPONIVEL','PROXIMO_LIMITE','CRITICO'))
);

CREATE TABLE COLETA (
  ID_COLETA            NUMBER GENERATED BY DEFAULT AS IDENTITY,
  ID_PONTO_COLETA      NUMBER NOT NULL,
  ID_RESIDUO           NUMBER NOT NULL,
  DT_COLETA            DATE DEFAULT SYSDATE NOT NULL,
  VOLUME_KG            NUMBER(12,2) NOT NULL,
  STATUS_COLETA        VARCHAR2(20) DEFAULT 'ABERTA' NOT NULL,
  DT_DESTINACAO        DATE,
  HISTORICO_DESTINACAO VARCHAR2(4000),
  CONSTRAINT PK_COLETA PRIMARY KEY (ID_COLETA),
  CONSTRAINT CK_COLETA_STATUS CHECK (STATUS_COLETA IN ('ABERTA','DESTINADA','CANCELADA')),
  CONSTRAINT FK_COLETA_PONTO FOREIGN KEY (ID_PONTO_COLETA) REFERENCES PONTO_COLETA(ID_PONTO_COLETA),
  CONSTRAINT FK_COLETA_RESIDUO FOREIGN KEY (ID_RESIDUO) REFERENCES RESIDUO(ID_RESIDUO)
);

CREATE TABLE DESTINACAO (
  ID_DESTINACAO       NUMBER GENERATED BY DEFAULT AS IDENTITY,
  ID_COLETA           NUMBER NOT NULL,
  DT_DESTINACAO       DATE DEFAULT SYSDATE NOT NULL,
  NOME_DESTINACAO     VARCHAR2(120) NOT NULL,
  TIPO_PROCESSAMENTO  VARCHAR2(60)  NOT NULL,
  VOLUME_KG_DESTINADO NUMBER(12,2)  NOT NULL,
  CONSTRAINT PK_DESTINACAO PRIMARY KEY (ID_DESTINACAO),
  CONSTRAINT FK_DESTINACAO_COLETA FOREIGN KEY (ID_COLETA) REFERENCES COLETA(ID_COLETA)
);

CREATE TABLE ALERTA_COLETA (
  ID_ALERTA       NUMBER GENERATED BY DEFAULT AS IDENTITY,
  ID_PONTO_COLETA NUMBER NOT NULL,
  ID_COLETA       NUMBER,
  DT_ALERTA       DATE DEFAULT SYSDATE NOT NULL,
  TIPO_ALERTA     VARCHAR2(30)  NOT NULL,
  MENSAGEM        VARCHAR2(300) NOT NULL,
  CONSTRAINT PK_ALERTA_COLETA PRIMARY KEY (ID_ALERTA),
  CONSTRAINT FK_ALERTA_PONTO FOREIGN KEY (ID_PONTO_COLETA) REFERENCES PONTO_COLETA(ID_PONTO_COLETA),
  CONSTRAINT FK_ALERTA_COLETA FOREIGN KEY (ID_COLETA) REFERENCES COLETA(ID_COLETA),
  CONSTRAINT CK_ALERTA_TIPO CHECK (TIPO_ALERTA IN ('LIMITE_ATINGIDO','CAPACIDADE_EXCEDIDA','INFORMACAO'))
);
